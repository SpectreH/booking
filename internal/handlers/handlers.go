// Repository is the repository type (Repository pattern)
type Repository struct {
	App *config.AppConfig
	DB  repository.DatabaseRepo
}

// SetNewRepo creates a new repository
func SetNewRepo(a *config.AppConfig, db *driver.DB) *Repository {
	return &Repository{
		App: a,
		DB:  dbrepo.SetNewPostgresRepo(db.SQL, a),
	}
}

// SetNewTestRepo creates a new testing repository
func SetNewTestRepo(a *config.AppConfig) *Repository {
	return &Repository{
		App: a,
		DB:  dbrepo.SetNewTestingRepo(a),
	}
}

